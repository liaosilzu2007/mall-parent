package com.lzumetal.mall.pojo;

/**
 * 用户
 *
 * @author liaosi
 * @date 2018-08-26
 */
public class User {

    private long id;

    private String phone;

    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
